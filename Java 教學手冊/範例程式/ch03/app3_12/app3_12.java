// app3_12, 強制轉換
public class app3_12
{
   public static void main(String args[])
   {
      int a=25;
      int b=9;

      System.out.println("a="+a+",b="+b); // 印出a、b的值
      System.out.println("a/b="+a/b);        // 印出a/b的值
      System.out.println("(float)a/b="+(float)a/b);
   }
}

/* app3_12 OUTPUT------------
a=25,b=9
a/b=2
(float)a/b=2.7777777
---------------------------*/