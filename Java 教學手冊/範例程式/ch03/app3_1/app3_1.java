// app3_1, 簡單的實例
public class app3_1
{
   public static void main(String args[])
   {
      int num=3;     // 宣告num為整數，並設值為3
      char ch='C';   // 宣告ch為字元變數，並設值為'C'
      System.out.println(num+" is an integer");
      System.out.println(ch+" is a character");
   }
}

/* app3_1 OUTPUT------------
3 is an integer
C is a character
---------------------------*/