// app3_11, 型態自動轉換
public class app3_11
{
   public static void main(String args[])
   {
      int a=45;         // 宣告a為整數
      float b=2.3f;     // 宣告b為浮點數

      System.out.println("a="+a+",b="+b); // 印出a、b的值
      System.out.println("a/b="+(a/b));      // 印出a/b的值
   }
}

/* app3_11 OUTPUT------------
a=45,b=2.3
a/b=19.565218
---------------------------*/