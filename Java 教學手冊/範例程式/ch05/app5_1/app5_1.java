// app5_1, if-else敘述
public class app5_1
{
   public static void main(String args[])
   {
      int a=15;

      if (a%2==0)        // 如果可被2整除
         System.out.println(a+" is an even number"); // 印出a為偶數
      else
         System.out.println(a+" is an odd number"); // 印出a為奇數
   }
}

/* app5_1 OUTPUT------
15 is an odd number
--------------------*/