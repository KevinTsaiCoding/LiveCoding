// app5_3,for迴圈
public class app5_3
{
   public static void main(String args[])
   {
      int i,sum=0;

      for(i=1;i<=10;i++)
         sum+=i;    // 計算sum=sum+i
      System.out.println("1+2+...+10="+sum);  // 印出結果
   }
}

/* app5_3 OUTPUT------
1+2+...+10=55
--------------------*/