// app4_3, 關係運算子
public class app4_3
{
   public static void main(String args[])
   {
      if (9>4)         // 判斷9>4是否成立
         System.out.println("9>4成立");           // 印出字串

      if (true)        // 判斷true是否成立
         System.out.println("此行會被執行--true");    // 印出字串

      if (false)        // 判斷false是否成立
         System.out.println("此行會被執行--false");   // 印出字串

      if (5==7)      // 判斷5是否等於7
         System.out.println("5==7成立");          // 印出字串
   }
}

/* app4_3 OUTPUT-----
9>4成立
此行會被執行--true
-------------------*/